public class Day33_operator_logika_NOT {
    public static void main (String [] args){
        int a = 10;
        int b = 5;

        // Menggunakan operator logika NOT (!)
        System.out.println("!(a > b): " + !(a > b)); // false
        System.out.println("!(a < b): " + !(a < b)); // true

    }

}
