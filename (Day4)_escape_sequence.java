public class main{
    public static void main(String[] args){
        System.out.println("Hello\nWorld"); // (\n) untuk membuat baris baru
        System.out.println("---------------------");
        System.out.println("Nama\t: Paduka"); // (\t) untuk menambahkan semacam tab
        System.out.println("Prodi\t: Informatika");
        System.out.println("---------------------");
        System.out.println("Dia berkata: \"Halo!\""); // (\")untuk menambahkan tanda kutip dalam sebuah string
    }
}