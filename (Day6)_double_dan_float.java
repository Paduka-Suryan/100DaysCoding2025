public class Main {
	public static void main(String[] args) {
		// Float menggunakan 32-bit dan memiliki presisi sekitar 7 digit desimal
		float a = 5.5555555f;
		
		// Double menggunakan 64-bit dan memberikan presisi lebih tinggi, sekitar 15 digit desimal
		double b = 2.222222222222222;
		
		System.out.print("Float \t: "+a+"\nDouble \t: "+b);
	}
}
