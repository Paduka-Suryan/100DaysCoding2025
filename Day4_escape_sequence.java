public class main{
    public static void main(String[] args){
        
        // (\n) untuk membuat baris baru
        System.out.println("Hello\nWorld");
        
        System.out.println("---------------------");
        
        // (\t) untuk membuat semacam tab
        System.out.println("Nama\t: Paduka");
        System.out.println("Prodi\t: Informatika");
        
        System.out.println("---------------------");
        
        // (\")untuk menambahkan tanda kutip dalam sebuah string
        System.out.println("Dia berkata: \"Halo!\"");
    }
}
