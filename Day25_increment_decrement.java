public class Day25_increment_decrement {
    public static void main(String[] args) {
        int a = 5;
        System.out.println("Nilai awal a: " + a);
        
        // Increment
        a++;
        System.out.println("Setelah increment (a++): " + a);
        
        // Decrement
        a--;
        System.out.println("Setelah decrement (a--): " + a);
    }
    
}
