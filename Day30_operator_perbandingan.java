public class Day30_operator_perbandingan {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int c = 10;

        System.out.println("a >= b: " + (a >= b)); // true
        System.out.println("a <= b: " + (a <= b)); // false
        System.out.println("a <= c: " + (a <= c)); // true
    }

}

